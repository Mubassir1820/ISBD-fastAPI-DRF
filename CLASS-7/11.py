character = input("Enter a character: ")

if character == 'a' or character == 'A':
    print("Vowel")
elif character == 'e' or character == 'E':
    print("Vowel")
elif character == 'i' or character == 'I':
    print("Vowel")
elif character == 'o' or character == 'O':
    print("Vowel")
elif character == 'u' or character == 'U':
    print("Vowel")
else:
    print('Consonent')